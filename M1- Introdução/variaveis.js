/* Exercício: Declarando Variáveis

Uma variável é um espaço de memória para armazenar valores.Podemos criá - los e, por sua vez,
também podemos atribuir - lhes um certo valor.

Em JavaScript, podemos declará - los usando a palavra
var e depois o nome que queremos dar.

Por exemplo:
    var preco;
Sua tarefa será declarar duas variáveis: uma com o nome idade e o outro com o nome peso.

Lembremos que, para criar uma variável, você deve usar a palavra reservada "var" 
seguida do nome com o qual você se referirá a ela.
*/


